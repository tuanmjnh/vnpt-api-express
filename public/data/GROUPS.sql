CREATE TABLE TTKD_BKN.GROUPS (
  ID         NUMBER         DEFAULT "TTKD_BKN"."CATEGORY_PK".nextval NOT NULL,
  APP_KEY    NVARCHAR2(50)  NOT NULL,
  CODE       NVARCHAR2(50),
  DEPENDENT  NUMBER         NOT NULL,
  LEVELS     NUMBER(5, 0)   NOT NULL,
  TITLE      NVARCHAR2(256) NOT NULL,
  ICON       NVARCHAR2(50),
  IMAGE      NVARCHAR2(512),
  URL        NVARCHAR2(512),
  ORDERS     NUMBER(5, 0)   NOT NULL,
  QUANTITY   NUMBER,
  POSITION   NVARCHAR2(128),
  DESCS      NVARCHAR2(512),
  CONTENT    NVARCHAR2(1024),
  TAGS       NVARCHAR2(128),
  START_AT   DATE,
  END_AT     DATE,
  CREATED_IP NVARCHAR2(128),
  CREATED_BY NVARCHAR2(256),
  CREATED_AT DATE,
  UPDATED_IP NVARCHAR2(50),
  UPDATED_BY NVARCHAR2(50),
  UPDATED_AT DATE,
  DELETED_IP NVARCHAR2(50),
  DELETED_BY NVARCHAR2(50),
  DELETED_AT DATE,
  FLAG       NUMBER(1, 0)   NOT NULL,
  ATTACH     VARCHAR2(128 BYTE),
  CONSTRAINT GROUPS_PK PRIMARY KEY (ID) USING INDEX TABLESPACE USERS STORAGE (INITIAL 64 K
                                                                              NEXT 1 M
                                                                              MAXEXTENTS UNLIMITED)
)
TABLESPACE USERS
STORAGE (INITIAL 64 K
         NEXT 1 M
         MAXEXTENTS UNLIMITED)
